package com.hsf.javagather.thread.five.one;

public final class Product {
    private final String no;
    private final String name;

    public Product(String no, String name) {
        super();
        this.no = no;
        this.name = name;
    }

    public String getNo() {
        return no;
    }

    public String getName() {
        return name;
    }
}
